def build_two_orbit(m, r_b, phi):
    """2m points: alternating outer (R=1) and inner (r_b) at offset pi/m + phi.
    Indices: outer i -> 2i, inner i -> 2i+1, so the natural cyclic order is
    A_0, B_0, A_1, B_1, ..."""
    pts = np.zeros((2 * m, 2))
    h = math.pi / m
    for i in range(m):
        ang_o = 2 * math.pi * i / m
        pts[2 * i] = (math.cos(ang_o), math.sin(ang_o))
        ang_i = ang_o + h + phi
        pts[2 * i + 1] = (r_b * math.cos(ang_i), r_b * math.sin(ang_i))
    return pts


def cyclic_strictly_convex(P, eps=1e-10):
    n = len(P)
    sgn = None
    for i in range(n):
        a = P[i]
        b = P[(i + 1) % n]
        c = P[(i + 2) % n]
        cross = (b[0] - a[0]) * (c[1] - b[1]) - (b[1] - a[1]) * (c[0] - b[0])
        if abs(cross) < eps:
            return False
        s = 1 if cross > 0 else -1
        if sgn is None:
            sgn = s
        elif s != sgn:
            return False
    return True


def all_vertex_4bad(P, tol=1e-6):
    n = len(P)
    # Pairwise distance matrix
    D = np.linalg.norm(P[:, None, :] - P[None, :, :], axis=2)
    for i in range(n):
        ds = np.sort(np.delete(D[i], i))
        # check largest cluster
        clusters = [[ds[0]]]
        for d in ds[1:]:
            if d - clusters[-1][-1] < tol:
                clusters[-1].append(d)
            else:
                clusters.append([d])
        if max(len(c) for c in clusters) < 4:
            return False
    return True


