def grid_scan_4bad(m, n_r=80, n_phi=41, tol_4bad=1e-6, eps_conv=1e-11):
    """Brute-force 2D grid scan over the convex window: find any (r, phi)
    inside (cos(pi/m), sec(pi/m)) x (-pi/m, pi/m) where the constructed
    polygon is strictly convex AND every vertex has multiplicity >= 4.
    Returns list of all such hits, plus the best near-miss."""
    cm = math.cos(math.pi / m)
    cM = 1.0 / cm
    rs = np.linspace(cm + 1e-3, cM - 1e-3, n_r)
    phis = np.linspace(-math.pi / m * 0.9999, math.pi / m * 0.9999, n_phi)
    hits = []
    best_near = None
    best_score = -1
    for r in rs:
        for phi in phis:
            pts = build_two_orbit(m, r, phi)
            if not cyclic_strictly_convex(pts, eps=eps_conv):
                continue
            # Compute multiplicities at every vertex
            n = len(pts)
            D = np.linalg.norm(pts[:, None, :] - pts[None, :, :], axis=2)
            min_mult = 1e9
            ok = True
            for i in range(n):
                ds = np.sort(np.delete(D[i], i))
                clusters = [[ds[0]]]
                for d in ds[1:]:
                    if d - clusters[-1][-1] < tol_4bad:
                        clusters[-1].append(d)
                    else:
                        clusters.append([d])
                mx = max(len(c) for c in clusters)
                min_mult = min(min_mult, mx)
                if mx < 4:
                    ok = False
            if ok:
                hits.append({"r": float(r), "phi": float(phi),
                             "min_mult": int(min_mult)})
            elif min_mult > best_score:
                best_score = int(min_mult)
                best_near = {"r": float(r), "phi": float(phi),
                             "min_mult": int(min_mult)}
    return hits, best_near


def search_m(m, tol_resid=1e-9, tol_4bad=1e-6, max_record=20):
    cm = math.cos(math.pi / m)
    cM = 1.0 / cm

    oa = outer_Oa_patterns(m)
    ob = outer_Ob_patterns(m)
    ia = inner_Ia_patterns(m)
    ib = inner_Ib_patterns(m)

    print(f"  patterns Oa={len(oa)} Ob={len(ob)} Ia={len(ia)} Ib={len(ib)}",
          flush=True)

    candidates = []

    # 1) Oa + Ia (most common)
    t0 = time.time()
    cands_aa = solve_combinations_Oa_Ia(m, oa, ia, cm, cM, tol_resid=tol_resid)
    print(f"  Oa+Ia: {len(cands_aa)} candidates ({time.time()-t0:.1f}s)", flush=True)
    candidates += cands_aa

    # 2) Ob + Ia
    t0 = time.time()
    cands_ba = solve_combinations_Ob_Ia(m, ob, ia, tol_resid=tol_resid)
    print(f"  Ob+Ia: {len(cands_ba)} candidates ({time.time()-t0:.1f}s)", flush=True)
    candidates += cands_ba

    # 3) Oa + Ib
    t0 = time.time()
    cands_ab = solve_combinations_Oa_Ib(m, oa, ib, tol_resid=tol_resid)
    print(f"  Oa+Ib: {len(cands_ab)} candidates ({time.time()-t0:.1f}s)", flush=True)
    candidates += cands_ab

    # 4) Ob + Ib (phi-only consistency)
    t0 = time.time()
    cands_bb = solve_combinations_Ob_Ib(m, ob, ib, tol_resid=tol_resid)
    print(f"  Ob+Ib: {len(cands_bb)} candidates with phi-consistent ({time.time()-t0:.1f}s)",
          flush=True)
    # for these the r is unconstrained: we will scan r to look for a strict-convex 4-bad sweet spot
    if cands_bb and len(cands_bb) <= 200:
        for c in cands_bb:
            phi = c["phi"]
            best = None
            for r in np.linspace(cm + 1e-3, cM - 1e-3, 31):
                pts = build_two_orbit(m, r, phi)
                if not cyclic_strictly_convex(pts):
                    continue
                # check 4-bad
                bad = all_vertex_4bad(pts, tol=tol_4bad)
                if bad:
                    best = (float(r), True)
                    break
            c["r_strict_convex_4bad"] = best
            if best is not None:
                c["r"] = best[0]
                candidates.append(c)

    # ----- Verify each candidate -----
    survivors = []
    near_misses = []
    n_in_window = 0
    n_strictly_convex = 0
    best_in_window_residual = math.inf
    best_record = None

    for c in candidates:
        r = c.get("r")
        phi = c.get("phi")
        if r is None or phi is None:
            continue
        in_window = (cm < r < cM)
        if in_window:
            n_in_window += 1
        pts = build_two_orbit(m, r, phi)
        conv = cyclic_strictly_convex(pts, eps=1e-11)
        if conv:
            n_strictly_convex += 1
        bad = all_vertex_4bad(pts, tol=tol_4bad)
        rec = {
            **c,
            "m": m,
            "n": 2 * m,
            "in_convex_window": in_window,
            "strictly_convex_alternating": conv,
            "all_vertex_4bad": bad,
            "convexity_window": [cm, cM],
        }
        # quantify residual: distance from chosen 4-equality to actual
        # use simple: take the witness sets and compute the 4 distances and
        # measure max - min spread.
        # (we recover the witness sets from outer_pattern / inner_pattern)
        op = c.get("outer_pattern")
        ip = c.get("inner_pattern")
        residual_spread = compute_witness_residual(op, ip, m, r, phi)
        rec["witness_residual_spread"] = residual_spread

        if in_window:
            if residual_spread < best_in_window_residual:
                best_in_window_residual = residual_spread
                best_record = rec
        if conv and bad and in_window:
            survivors.append(rec)
        elif in_window and (conv or bad):
            near_misses.append(rec)

    # Also do brute-force 2D grid scan in the convex window
    t0 = time.time()
    grid_hits, grid_best_near = grid_scan_4bad(m, n_r=60, n_phi=31, tol_4bad=tol_4bad)
    print(f"  grid scan: {len(grid_hits)} hits, best near-miss min_mult="
          f"{grid_best_near['min_mult'] if grid_best_near else None} "
          f"({time.time()-t0:.1f}s)", flush=True)

    return {
        "m": m,
        "n": 2 * m,
        "convex_window": [cm, cM],
        "n_candidates": len(candidates),
        "n_in_convex_window": n_in_window,
        "n_strictly_convex": n_strictly_convex,
        "survivors": survivors,
        "near_misses_in_window": near_misses[:max_record],
        "best_record_in_window": best_record,
        "best_residual_in_window": (
            None if best_record is None else best_in_window_residual
        ),
        "grid_scan_hits": grid_hits,
        "grid_scan_best_near_miss": grid_best_near,
    }


def compute_witness_residual(outer_pattern, inner_pattern, m, r, phi):
    """Return the worst-case |D - mean| spread over the union witness set,
    a small number = the chosen 4 distances are nearly equal at A_0 / B_0."""
    if outer_pattern is None or inner_pattern is None:
        return None
    out_kind = outer_pattern[0]
    in_kind = inner_pattern[0]
    pts_outer_d2 = []
    pts_inner_d2 = []
    if out_kind == "Oa":
        a_O, bO1, bO2 = outer_pattern[1], outer_pattern[2], outer_pattern[3]
        pts_outer_d2 = [
            D_AA(a_O, m),
            D_AA((m - a_O) % m, m),
            D_AB(bO1, m, r, phi),
            D_AB(bO2, m, r, phi),
        ]
    elif out_kind == "Ob":
        b1a, b1b, b2a, b2b = outer_pattern[1:]
        pts_outer_d2 = [
            D_AB(b1a, m, r, phi),
            D_AB(b1b, m, r, phi),
            D_AB(b2a, m, r, phi),
            D_AB(b2b, m, r, phi),
        ]
    if in_kind == "Ia":
        a_I, bI1, bI2 = inner_pattern[1], inner_pattern[2], inner_pattern[3]
        pts_inner_d2 = [
            D_BB(a_I, m, r),
            D_BB((m - a_I) % m, m, r),
            D_BA(bI1, m, r, phi),
            D_BA(bI2, m, r, phi),
        ]
    elif in_kind == "Ib":
        b1a, b1b, b2a, b2b = inner_pattern[1:]
        pts_inner_d2 = [
            D_BA(b1a, m, r, phi),
            D_BA(b1b, m, r, phi),
            D_BA(b2a, m, r, phi),
            D_BA(b2b, m, r, phi),
        ]
    res_o = max(pts_outer_d2) - min(pts_outer_d2) if pts_outer_d2 else 0.0
    res_i = max(pts_inner_d2) - min(pts_inner_d2) if pts_inner_d2 else 0.0
    return float(max(res_o, res_i))


