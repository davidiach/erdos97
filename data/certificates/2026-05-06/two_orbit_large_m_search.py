def quad_roots(a, b, c):
    """Real roots of a x^2 + b x + c, returned sorted ascending. NaN if none."""
    if abs(a) < 1e-15:
        if abs(b) < 1e-15:
            return []
        return [-c / b]
    disc = b * b - 4 * a * c
    if disc < -1e-14:
        return []
    if disc < 0:
        disc = 0.0
    sd = math.sqrt(disc)
    return sorted([(-b - sd) / (2 * a), (-b + sd) / (2 * a)])


def solve_Oa_for_r(a, b1, m, phi):
    """Outer 'Oa' equation D_AA(a) = D_AB(b1) =>
       r^2 - 2 cos(2 pi b1/m + pi/m + phi) r + (1 - D_AA(a)) = 0
       i.e. r^2 - 2 c r + (1 - 2 + 2 cos(2 pi a/m)) = 0
            r^2 - 2 c r + (2 cos(2 pi a/m) - 1) = 0
    """
    c_O = math.cos(2 * math.pi * b1 / m + math.pi / m + phi)
    cst = 2 * math.cos(2 * math.pi * a / m) - 1
    return quad_roots(1.0, -2 * c_O, cst)


def solve_Ia_for_r(a, b1, m, phi):
    """Inner 'Ia' equation D_BB(a) = D_BA(b1):
       2 r^2 (1 - cos(2 pi a/m)) - (1 + r^2 - 2 r cos(2 pi b1/m - pi/m - phi)) = 0
       (2 (1 - cos t_a) - 1) r^2 + 2 cos(b1) r - 1 = 0
       (1 - 2 cos t_a) r^2 + 2 c_I r - 1 = 0
    """
    A = 1.0 - 2.0 * math.cos(2 * math.pi * a / m)
    c_I = math.cos(2 * math.pi * b1 / m - math.pi / m - phi)
    return quad_roots(A, 2 * c_I, -1.0)


def solve_Ob_for_phi_candidates(b1, b2, m):
    """Outer 'Ob' equation: cos(2 pi b1/m + pi/m + phi) = cos(2 pi b2/m + pi/m + phi).
    Let u_i = 2 pi b_i/m + pi/m. cos(u1+phi) = cos(u2+phi) ->
       u1+phi = +-(u2+phi) + 2k pi.
    The + branch -> u1=u2 (impossible since b1 != b2).
    The - branch -> phi = -(u1+u2)/2 + k pi  (mod 2 pi).
    Return the unique-modulo-pi value(s) in (-pi/m, pi/m].
    """
    u1 = 2 * math.pi * b1 / m + math.pi / m
    u2 = 2 * math.pi * b2 / m + math.pi / m
    base = -(u1 + u2) / 2.0
    cands = []
    for k in range(-3, 4):
        phi = base + k * math.pi
        # reduce into (-pi/m, pi/m]
        # We want abs(phi) <= pi/m * 0.9999 typically
        cands.append(phi)
    return cands


def solve_Ib_for_phi_candidates(b1, b2, m):
    """Inner 'Ib' equation: cos(2 pi b1/m - pi/m - phi) = cos(2 pi b2/m - pi/m - phi).
    Let u_i = 2 pi b_i/m - pi/m. cos(u1-phi)=cos(u2-phi) ->
       u1-phi = +-(u2-phi) + 2k pi.
    + branch: u1=u2 (impossible).
    - branch: u1 - phi = -(u2-phi) + 2k pi -> 2 phi = u1 + u2 + ... wait
       u1 - phi = -u2 + phi + 2k pi  -> 2 phi = u1 + u2 - 2 k pi
       phi = (u1+u2)/2 - k pi
    """
    u1 = 2 * math.pi * b1 / m - math.pi / m
    u2 = 2 * math.pi * b2 / m - math.pi / m
    base = (u1 + u2) / 2.0
    cands = []
    for k in range(-3, 4):
        phi = base + k * math.pi
        cands.append(phi)
    return cands


# ------------------------------------------------------------------
# Distance functions for residual / verification
# ------------------------------------------------------------------

def D_AA(a, m):
    return 2.0 - 2.0 * math.cos(2 * math.pi * a / m)


def D_AB(b, m, r, phi):
    return 1.0 + r * r - 2.0 * r * math.cos(2 * math.pi * b / m + math.pi / m + phi)


def D_BB(a, m, r):
    return 2.0 * r * r * (1.0 - math.cos(2 * math.pi * a / m))


def D_BA(b, m, r, phi):
    return 1.0 + r * r - 2.0 * r * math.cos(2 * math.pi * b / m - math.pi / m - phi)


