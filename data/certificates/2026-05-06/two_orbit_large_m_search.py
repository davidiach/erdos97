def all_outer_a_for_phi_r(m, phi, r):
    """For diagnostic: which outer pair-classes equal D_AB(b1) for this (r, phi)?"""
    d_aa = [D_AA(a, m) for a in range(m)]
    return d_aa


def normalize_phi(phi, m):
    """Reduce phi into the interval (-pi/m, pi/m]."""
    P = 2 * math.pi / m
    phi_n = ((phi + math.pi / m) % P) - math.pi / m
    return phi_n


def solve_combinations_Oa_Ia(m, oa_list, ia_list, cm, cM, tol_resid=1e-9, n_seed=11):
    """For each (Oa) outer eq + (Ia) inner eq, the system has 2 unknowns (r, phi)
    and 2 equations. Use polynomial elimination:
      Oa: r^2 - 2 r cos(u_O + phi) + (2 cos t_a - 1) = 0
      Ia: (1 - 2 cos t_{a'}) r^2 + 2 r cos(u_I - phi) - 1 = 0
    where u_O = 2 pi b1_O/m + pi/m, u_I = 2 pi b1_I/m - pi/m.
    Strategy: parametrise phi over a fine 1D grid, solve quadratic for r at each
    phi from the Oa equation, then check Ia residual; collect (r, phi) where Ia
    residual ~= 0 by Newton. With closed-form quadratic + Newton in 1D this is
    fast.
    Returns list of (r, phi, oa, ia, residual) survivors.
    """
    survivors = []
    # phi-grid (we will refine each)
    phi_grid = np.linspace(-math.pi / m * 0.9999, math.pi / m * 0.9999, n_seed * 4)
    for (a_O, bO1, bO2) in oa_list:
        cst_O = 2 * math.cos(2 * math.pi * a_O / m) - 1
        u_O = 2 * math.pi * bO1 / m + math.pi / m
        for (a_I, bI1, bI2) in ia_list:
            A_I = 1.0 - 2.0 * math.cos(2 * math.pi * a_I / m)
            u_I = 2 * math.pi * bI1 / m - math.pi / m
            # Define h(phi) = Ia residual using the r from Oa
            def Hboth(phi):
                # roots of Oa quadratic
                rr = quad_roots(1.0, -2 * math.cos(u_O + phi), cst_O)
                if not rr:
                    return None
                vals = []
                for r in rr:
                    # Ia residual
                    if r <= 0 or r > 100:
                        continue
                    val = A_I * r * r + 2 * r * math.cos(u_I - phi) - 1.0
                    vals.append((r, val))
                return vals

            # Sample on grid: track residual sign
            grid_eval = []
            for phi in phi_grid:
                pairs = Hboth(phi)
                if pairs is None:
                    continue
                grid_eval.append((phi, pairs))
            # Find sign changes per "branch index" 0,1
            for branch in (0, 1):
                seq = []
                for phi, pairs in grid_eval:
                    if branch < len(pairs):
                        seq.append((phi, pairs[branch][0], pairs[branch][1]))
                # bisect on each consecutive sign change
                for k in range(len(seq) - 1):
                    phi_a, _, va = seq[k]
                    phi_b, _, vb = seq[k + 1]
                    if math.isnan(va) or math.isnan(vb):
                        continue
                    if va * vb > 0:
                        continue
                    # bisect in phi
                    lo, hi = phi_a, phi_b
                    flo = va
                    _ = vb
                    for _ in range(80):
                        mid = (lo + hi) / 2
                        pairs = Hboth(mid)
                        if pairs is None or branch >= len(pairs):
                            break
                        rmid, fmid = pairs[branch]
                        if abs(fmid) < tol_resid:
                            break
                        if flo * fmid <= 0:
                            hi = mid
                        else:
                            lo, flo = mid, fmid
                    pairs = Hboth(mid)
                    if pairs is None or branch >= len(pairs):
                        continue
                    rmid, fmid = pairs[branch]
                    if abs(fmid) > tol_resid:
                        continue
                    if rmid <= 0:
                        continue
                    survivors.append({
                        "outer_pattern": ("Oa", a_O, bO1, bO2),
                        "inner_pattern": ("Ia", a_I, bI1, bI2),
                        "r": rmid,
                        "phi": normalize_phi(mid, m),
                        "phi_unnormalized": mid,
                        "Ia_residual": fmid,
                    })
    return survivors


def solve_combinations_Ob_Ia(m, ob_list, ia_list, tol_resid=1e-9):
    """Outer 'Ob' (block of 4 inner) gives a closed-form phi.
    Then Ia gives a quadratic in r: (1-2 cos t_a) r^2 + 2 r cos(u_I - phi) - 1 = 0.
    """
    survivors = []
    for ((bO1a, bO1b), (bO2a, bO2b)) in ob_list:
        phi_cands = solve_Ob_for_phi_candidates(bO1a, bO2a, m)
        for phi_raw in phi_cands:
            phi = normalize_phi(phi_raw, m)
            if abs(phi) > math.pi / m * 0.9999:
                continue
            for (a_I, bI1, bI2) in ia_list:
                A_I = 1.0 - 2.0 * math.cos(2 * math.pi * a_I / m)
                u_I = 2 * math.pi * bI1 / m - math.pi / m
                rs = quad_roots(A_I, 2 * math.cos(u_I - phi), -1.0)
                for r in rs:
                    if r <= 0 or r > 100:
                        continue
                    # verify Ob residual
                    res_Ob = (
                        D_AB(bO1a, m, r, phi) - D_AB(bO2a, m, r, phi)
                    )
                    if abs(res_Ob) > tol_resid:
                        continue
                    survivors.append({
                        "outer_pattern": ("Ob", bO1a, bO1b, bO2a, bO2b),
                        "inner_pattern": ("Ia", a_I, bI1, bI2),
                        "r": r,
                        "phi": phi,
                        "Ob_residual": res_Ob,
                    })
    return survivors


def solve_combinations_Oa_Ib(m, oa_list, ib_list, tol_resid=1e-9):
    """Inner 'Ib' gives closed-form phi. Then Oa gives a quadratic in r."""
    survivors = []
    for ((bI1a, bI1b), (bI2a, bI2b)) in ib_list:
        phi_cands = solve_Ib_for_phi_candidates(bI1a, bI2a, m)
        for phi_raw in phi_cands:
            phi = normalize_phi(phi_raw, m)
            if abs(phi) > math.pi / m * 0.9999:
                continue
            for (a_O, bO1, bO2) in oa_list:
                cst_O = 2 * math.cos(2 * math.pi * a_O / m) - 1
                u_O = 2 * math.pi * bO1 / m + math.pi / m
                rs = quad_roots(1.0, -2 * math.cos(u_O + phi), cst_O)
                for r in rs:
                    if r <= 0 or r > 100:
                        continue
                    res_Ib = (
                        D_BA(bI1a, m, r, phi) - D_BA(bI2a, m, r, phi)
                    )
                    if abs(res_Ib) > tol_resid:
                        continue
                    survivors.append({
                        "outer_pattern": ("Oa", a_O, bO1, bO2),
                        "inner_pattern": ("Ib", bI1a, bI1b, bI2a, bI2b),
                        "r": r,
                        "phi": phi,
                        "Ib_residual": res_Ib,
                    })
    return survivors


def solve_combinations_Ob_Ib(m, ob_list, ib_list, tol_resid=1e-9):
    """Both phi-only equations: succeed only if same phi works for both."""
    survivors = []
    # precompute phi for each Ob, Ib
    ob_phis = {idx: solve_Ob_for_phi_candidates(b[0][0], b[1][0], m)
               for idx, b in enumerate(ob_list)}
    ib_phis = {idx: solve_Ib_for_phi_candidates(b[0][0], b[1][0], m)
               for idx, b in enumerate(ib_list)}
    for io, ob in enumerate(ob_list):
        for ii, ib in enumerate(ib_list):
            for poa in ob_phis[io]:
                for pib in ib_phis[ii]:
                    if abs(normalize_phi(poa - pib, m)) > 1e-6:
                        continue
                    phi = normalize_phi(poa, m)
                    if abs(phi) > math.pi / m * 0.9999:
                        continue
                    # r free: any r in window. Try midpoint and refine? r unconstrained
                    # by these equations alone, so we don't have a unique r.
                    # In practice all-vertex 4-bad will then determine r via additional
                    # forced equalities. We skip this case to avoid spurious survivors;
                    # user can examine grid scan post-hoc.
                    survivors.append({
                        "outer_pattern": ("Ob", ob[0][0], ob[0][1], ob[1][0], ob[1][1]),
                        "inner_pattern": ("Ib", ib[0][0], ib[0][1], ib[1][0], ib[1][1]),
                        "r": None,  # unconstrained
                        "phi": phi,
                        "phi_consistent": True,
                    })
    return survivors


