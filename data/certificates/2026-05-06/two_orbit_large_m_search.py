def outer_Oa_patterns(m):
    """List of (a, b1, b2). a in [1, m//2], 2a != m. b1 < b2, b1+b2 == -1 mod m."""
    out = []
    half = m // 2
    for a in range(1, half + 1):
        if 2 * a == m:
            continue
        for b1 in range(m):
            b2 = (-1 - b1) % m
            if b1 < b2:
                out.append((a, b1, b2))
    return out


def outer_Ob_patterns(m):
    """List of ((b1a, b1b), (b2a, b2b)) for distinct outer-block pair-classes."""
    pcs = []
    for b1 in range(m):
        b2 = (-1 - b1) % m
        if b1 < b2:
            pcs.append((b1, b2))
    return list(combinations(pcs, 2))


def inner_Ia_patterns(m):
    out = []
    half = m // 2
    for a in range(1, half + 1):
        if 2 * a == m:
            continue
        for b1 in range(m):
            b2 = (1 - b1) % m
            if b1 < b2:
                out.append((a, b1, b2))
    return out


def inner_Ib_patterns(m):
    pcs = []
    for b1 in range(m):
        b2 = (1 - b1) % m
        if b1 < b2:
            pcs.append((b1, b2))
    return list(combinations(pcs, 2))


